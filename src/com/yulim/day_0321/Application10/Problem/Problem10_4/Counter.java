package com.yulim.day_0321.Application10.Problem.Problem10_4;

public class Counter {
    private int count = 0;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void count() {
        count++;
    }

}


class DownCounter {
    private int count = 0;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void count() {
        count--;
    }

}
