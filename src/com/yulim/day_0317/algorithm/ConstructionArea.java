package com.yulim.day_0317.algorithm;

public class ConstructionArea {
	private int x;
	private int y;
	private int noise;

	ConstructionArea(int x, int y, int noise) {
		this.x = x;
		this.y = y;
		this.noise = noise;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getNoise() {
		return noise;
	}
}
