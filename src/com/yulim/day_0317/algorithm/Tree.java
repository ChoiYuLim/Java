package com.yulim.day_0317.algorithm;

public class Tree {

	private int x;
	private int y;
	private int count;

	Tree(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
